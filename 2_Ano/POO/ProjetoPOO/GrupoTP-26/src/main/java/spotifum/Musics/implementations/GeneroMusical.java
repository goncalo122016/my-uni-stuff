package spotifum.Musics.implementations;

/**
* Enum representing different musical genres available in the system.
* This enum defines the various categories of music that can be assigned to songs.
*/
public enum GeneroMusical {
   /**
    * Represents the Pop music genre.
    */
   POP,
   
   /**
    * Represents the Rock music genre.
    */
   ROCK,
   
   /**
    * Represents the Classical music genre.
    */
   CLASSICA,
   
   /**
    * Represents the Jazz music genre.
    */
   JAZZ,
   
   /**
    * Represents the Hip Hop music genre.
    */
   HIPHOP,
   
   /**
    * Represents the Electronic music genre.
    */
   ELETRONICA,
   
   /**
    * Represents the Reggae music genre.
    */
   REGGAE,
   
   /**
    * Represents the Fado music genre.
    */
   FADO,
   
   /**
    * Represents other music genres not specifically categorized.
    */
   OUTRO
}