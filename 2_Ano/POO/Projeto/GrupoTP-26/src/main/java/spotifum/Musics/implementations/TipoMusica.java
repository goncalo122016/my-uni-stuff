package spotifum.Musics.implementations;

/**
 * Enumeração que define os tipos de música disponíveis.
 */
public enum TipoMusica {
    /**
     * Música com conteúdo multimédia, com vídeo associado.
     */
    MULTIMEDIA,

    /**
     * Música normal.
     */
    NORMAL,

    /**
     * Música com conteúdo explícito.
     */
    EXPLICITA
}
